const menuBtn = document.getElementById("menuBtn");
const mobileMenu = document.getElementById("mobileMenu");
const langButtons = document.querySelectorAll(".lang-btn");

function setLanguage(lang) {
  const selectedLanguage = translations[lang] ? lang : "tr";

  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.getAttribute("data-i18n");

    if (translations[selectedLanguage][key]) {
      element.innerHTML = translations[selectedLanguage][key];
    }
  });

  document.documentElement.lang = selectedLanguage;
  localStorage.setItem("siteLanguage", selectedLanguage);

  langButtons.forEach(button => {
    button.classList.toggle("active", button.dataset.lang === selectedLanguage);
  });
}

menuBtn.addEventListener("click", () => {
  mobileMenu.classList.toggle("active");
  menuBtn.textContent = mobileMenu.classList.contains("active") ? "✕" : "☰";
});

document.querySelectorAll("#mobileMenu a").forEach(link => {
  link.addEventListener("click", () => {
    mobileMenu.classList.remove("active");
    menuBtn.textContent = "☰";
  });
});

langButtons.forEach(button => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
    mobileMenu.classList.remove("active");
    menuBtn.textContent = "☰";
  });
});

const savedLanguage = localStorage.getItem("siteLanguage") || "tr";
setLanguage(savedLanguage);

